public class MinStock {
	private Code dc;
	private Integer quantity;

	public MinStock(){
		
	}
	
	public MinStock(Code dc, Integer quantity) {
		this.dc = dc;
		this.quantity = quantity;
	}

	public Code getDc() {
		return dc;
	}

	public void setDc(Code dc) {
		this.dc = dc;
	}

	public Integer getQuantity() {
		return quantity;
	}

	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}

}